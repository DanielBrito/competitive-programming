#include <cmath> // Include the cmath library
#include <iostream>

using namespace std;

int main(){

  cout << sqrt(64) << endl;             // 8
  cout << round(2.6) << endl;           // 3
  cout << log(2) << endl;               // 0.693147
  cout << abs(-10) << endl;             // 10
  cout << cbrt(8) << endl;              // 2
  cout << ceil(10.2598) << endl;        // 11
  cout << fdim(50, 20) << endl;         // 30
  cout << floor(3.897) << endl;         // 3
  cout << max(10, 45) << endl;          // 45
  cout << min(10, 45) << endl;          // 10
  cout << fmax(15.987, 14.789) << endl; // 15.987
  cout << fmin(15.987, 14.789) << endl; // 14.789
  cout << fmod(10, 4) << endl;          // 2
  cout << pow(2, 10) << endl;           // 1024

  return 0;
}